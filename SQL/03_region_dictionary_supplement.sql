SELECT '=== 区域字典数据统计 ===' AS info;
SELECT 
    region_level AS '层级',
    COUNT(*) AS '数量'
FROM region_dictionary 
WHERE is_deleted = 0 
GROUP BY region_level 
ORDER BY region_level;

SELECT '=== 省级行政区列表 ===' AS info;
SELECT 
    region_code AS '区域代码',
    region_name AS '区域名称',
    sort_order AS '排序'
FROM region_dictionary 
WHERE is_deleted = 0 AND region_level = 1 
ORDER BY sort_order;