SELECT '--- 各省区县数据统计 ---' AS '';
SELECT 
    p.region_name AS '省份',
    COUNT(d.id) AS '区县数量'
FROM region_dictionary p
LEFT JOIN region_dictionary c ON c.parent_code = p.region_code AND c.is_deleted = 0 AND c.region_level = 2
LEFT JOIN region_dictionary d ON d.parent_code = c.region_code AND d.is_deleted = 0 AND d.region_level = 3
WHERE p.is_deleted = 0 AND p.region_level = 1
GROUP BY p.region_code, p.region_name
HAVING COUNT(d.id) > 0
ORDER BY COUNT(d.id) DESC;